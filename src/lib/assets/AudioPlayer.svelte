<script lang="ts">
	import RangeInput from './RangeInput.svelte';
	import { setSharedTrackData } from './SharedData.svelte';

	let audioElement: HTMLAudioElement;
	let isPlaying: boolean = $state(false);
	let currentTime: number = $state(0);
	let duration: number = $state(0);
	let speed: number = $state(1);
	let volume: number = $state(0.25);
	let previousVolume: number = $state(0.5);
	let isMuted: boolean = $state(false);
	let animationFrameId: number;
	let fileInput: HTMLInputElement;
	let selectedFile: File | null = null;
	let audioUrl: string | null = $state(null);

	function handleFileSelect(event: Event): void {
		const target = event.target as HTMLInputElement;
		if (target.files && target.files.length > 0) {
			const file = target.files[0];
			if (file.type.startsWith('audio/')) {
				selectedFile = file;
				if (audioUrl) {
					URL.revokeObjectURL(audioUrl);
				}
				audioUrl = URL.createObjectURL(file);
			}
		}
	}

	function triggerFileInput(): void {
		fileInput.click();
	}

	function toggleMute(toggle: boolean): void {
		if (toggle) {
			previousVolume = volume;
			volume = 0;
			isMuted = true;
		} else {
			volume = previousVolume;
			isMuted = false;
		}
		audioElement.volume = volume;
	}

	function togglePlay(): void {
		if (duration <= 0) {
			return;
		}
		if (isPlaying) {
			audioElement.pause();
		} else {
			audioElement.volume = volume; //To make sure volume matches
			audioElement.play();
		}
		isPlaying = !isPlaying;
	}

	function updateTime(): void {
		if (audioElement && !audioElement.paused) {
			currentTime = audioElement.currentTime;
			animationFrameId = requestAnimationFrame(updateTime);
		}
	}

	function checkIfPlaying(): void {
		if (isPlaying) {
			updateTime();
		} else {
			if (animationFrameId) {
				cancelAnimationFrame(animationFrameId);
			}
			currentTime = audioElement.currentTime;
		}
	}

	function loadDuration(): void {
		duration = Math.round(audioElement.duration);
		setSharedTrackData({duration:duration})
	}

	function handleSeek(event: Event): void {
		const input = event.target as HTMLInputElement;
		audioElement.currentTime = parseFloat(input.value);
		currentTime = audioElement.currentTime;
	}

	function changeSpeed(event: Event): void {
		const input = event.target as HTMLInputElement;
		speed = parseFloat(input.value);
		audioElement.playbackRate = speed;
	}

	function changeVolume(event: Event): void {
		const input = event.target as HTMLInputElement;
		volume = parseFloat(input.value);
		audioElement.volume = volume;
	}

	function formatTime(seconds: number): string {
		const mins = Math.floor(seconds / 60);
		const secs = Math.floor(seconds % 60);
		const centiseconds = Math.floor((seconds % 1) * 100);
		return `${mins.toString().padStart(2, '0')}:${secs.toString().padStart(2, '0')}.${centiseconds.toString().padStart(2, '0')}`;
	}

	$effect(() => {
		checkIfPlaying();
	});
</script>

